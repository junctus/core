//! `neo-credits` — anonymous bandwidth credits (frontier).
//!
//! Unlinkable, token-free credits: a node **earns** credits by relaying and
//! **spends** them to send. Because earning a credit costs real relayed
//! bandwidth, faking N nodes costs N nodes' worth of bandwidth — one mechanism
//! that resists both Sybil attacks and free-riding. Built on blind-signature /
//! anonymous-credential schemes (Coconut / Privacy Pass lineage). Deliberately
//! **no blockchain token**.
//!
//! Status: stub — implemented in milestone M10.
